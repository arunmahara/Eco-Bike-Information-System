    private void csvImporter() {
        JFileChooser fc = new JFileChooser(System.getProperty("user.dir"));
        fc.setDialogTitle("Import");
        int i = fc.showOpenDialog(this);
        if (i == JFileChooser.APPROVE_OPTION) {
            File f = fc.getSelectedFile();
            String filepath = f.getPath();
            if (openFile(filepath)) {
                JOptionPane.showMessageDialog(null, f.getName() + " is imported succesfully");
            } else {
                JOptionPane.showMessageDialog(null, f.getName() + " was not imported");
            }
        }
    }

    private boolean openFile(String filePath) {
        ArrayList<Object[]> rows = new ArrayList<>();
        boolean success = false;
        BufferedReader br = null;
        String line = "";

        try {
            br = new BufferedReader(new FileReader(filePath));
            ArrayList<Integer> idHolder = new ArrayList<>();
            while ((line = br.readLine()) != null) {

                String[] row = line.split(",");

                for (String each : row) {
                    System.out.println(each);
                }

                int bikeID = Integer.parseInt(row[0]);
                String model_name = row[1];
                String category = row[2];
                String brand = row[3];
                int price = Integer.parseInt(row[4]);
                String material = row[5];

                Object[] rowData = {bikeID, model_name, category, brand, price, material};

                boolean valid = true;
                if (idHolder.contains(bikeID)) {
                    valid = false;
                }
                if (valid) {
                    rows.add(rowData);
                } else {
                    throw new DoubleIDException();
                }
                idHolder.add(bikeID);
            }
            DefaultTableModel model = (DefaultTableModel) current_records_table.getModel();
            model.setRowCount(0);

            for (Object[] row : rows) {
                model.addRow(row);
            }

            success = true;
        } catch (DoubleIDException e) {
            JOptionPane.showMessageDialog(this, "Your CSV file contains repeated ID numbers.", "Failure", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(this, "Incorrect csv format or file format \n Please refer to help section on what type of csv files you can import", "Failure", JOptionPane.ERROR_MESSAGE);
            success = false;
        } finally {
            if (br != null) {
                try {
                    br.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            return success;
        }
    }

    private void saveFile(File file) throws IOException {
        Path path = file.toPath();
        BufferedWriter bw = null;
        try {
            bw = Files.newBufferedWriter(path,
                    StandardCharsets.UTF_8, StandardOpenOption.CREATE);

            DefaultTableModel model = (DefaultTableModel) current_records_table.getModel();
            String rows = "";
            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    rows += model.getValueAt(i, j) + ",";
                }
                rows += "\n";
            }
            bw.write(rows);

        } catch (IOException e) {
            System.out.println(e.getStackTrace());
        } finally {
            bw.flush();
        }
    }

    private void displayInTable(String selectedItem) {
        DefaultTableModel model = (DefaultTableModel) current_records_table.getModel();

        ArrayList<Integer> ids = new ArrayList<>();

        for (int i = 0; i < model.getRowCount(); i++) {
            if (selectedItem.equals(model.getValueAt(i, 2))) {
                ids.add(i);
            }

        }

        Object[][] rows = new Object[ids.size()][model.getColumnCount()];

        for (int r = 0; r < ids.size(); r++) {
            for (int j = 0; j < model.getColumnCount(); j++) {
                rows[r][j] = model.getValueAt(ids.get(r), j);
            }
        }
        if (ids.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No bikes found on this category");
        } else {
            Object[] cols = {
                "Id", "Model Name", "Category", "Brand", "Price", "Material"
            };
            JTable msgbox_table = new JTable(rows, cols);
            JOptionPane.showMessageDialog(null, new JScrollPane(msgbox_table), "Available Bikes on this category", JOptionPane.PLAIN_MESSAGE);
        }
    }

