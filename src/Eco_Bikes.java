    private void pricesearch_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pricesearch_buttonActionPerformed
        // TODO add your handling code here:

        if (!searchprice_textfield.getText().isEmpty()) {
            DefaultTableModel model = (DefaultTableModel) current_records_table.getModel();
            int[] prices_array = new int[(current_records_table.getModel().getRowCount())];
            HashMap<Integer, Integer> hm = new HashMap<>();
            int[][] a2d = new int[model.getRowCount()][1];
            for (int i = 0; i < current_records_table.getModel().getRowCount(); i++) {
                prices_array[i] = (Integer.parseInt(model.getValueAt(i, 4).toString())); //get the all row values at column index 0
                if (!hm.containsKey(prices_array[i])) {
                    hm.put(prices_array[i], i);
                    a2d[i][0] = prices_array[i];
                }
            }
            System.out.println("Unsorted Price: " + Arrays.toString(prices_array));
            AscendingSorter.sort(prices_array);
            System.out.println("Sorted Price : " + Arrays.toString(prices_array));

            //sorted price by ascending order above, now binary search below:
            int userPrice = Integer.parseInt(searchprice_textfield.getText());
            System.out.println("Searched Price: " + userPrice);
            if (BinarySearch.binarySearcher(prices_array, userPrice)) { // condition if searched item is found
                
                Object[][] rows = new Object[1][model.getColumnCount()];
                
                for (int j = 0; j < model.getColumnCount(); j++) {
                    rows[0][j] = model.getValueAt(hm.get(userPrice), j);
                }
                
                Object[] cols = {
                    "Id", "Model Name", "Category", "Brand", "Price", "Material"
                };
                JTable searchPrice_table = new JTable(rows, cols);
                JOptionPane.showMessageDialog(null, new JScrollPane(searchPrice_table), "Available Bike on this price", JOptionPane.PLAIN_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "Sorry, there are no any bikes in this price.");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please provide your prefered price before searcing");
        }
    }//GEN-LAST:event_pricesearch_buttonActionPerformed

